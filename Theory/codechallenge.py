def sum(a, b):
    return float(a) + float(b)


def minus(a, b):
    return float(a) - float(b)


def mul(a, b):
    return float(a) * float(b)


def div(a, b):
    return float(a) / float(b)


def power(a, b):
    return float(a) ** float(b)


def remainder(a, b):
    return float(a) // float(b)


print(sum(3, 4))
print(minus(3, 4))
print(mul(3, 4))
print(div(3, 4))
print(power(3, 4))
print(remainder(3, 4))
